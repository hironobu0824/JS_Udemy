// nullとundifined
// nullは予定通り何もない。
// undefindは不測の事態で何もない。

// typeof 演算子
typeof 3
// 'number'
typeof -5
// 'number'
typeof 3.2
// 'number'
typeof NaN
// 'number'
typeof true
// 'boolean'
typeof 'hello'
// 'string'
typeof { name: 'hironobu' }
// 'object'
typeof [1,2,3]
// 'object'
typeof undefined
// 'undefined'
typeof null
// 'object'

